// Instâncias dos Contratos
const nftContract = new web3.eth.Contract(nftAbi, nftContractAddress);
const tokenContract = new web3.eth.Contract(tokenAbi, tokenContractAddress);

// Função para conectar ao MetaMask
async function connectWallet() {
    if (window.ethereum) {
        try {
            await window.ethereum.request({ method: "eth_requestAccounts" });
            alert("Conectado ao MetaMask!");
        } catch (error) {
            alert("Erro ao conectar ao MetaMask.");
        }
    } else {
        alert("MetaMask não detectado.");
    }
}

// Função para criar um NFT
async function mintNFT() {
    const recipient = document.getElementById("nftRecipient").value;
    const metadata = document.getElementById("nftMetadata").value;

    const accounts = await web3.eth.getAccounts();
    try {
        await nftContract.methods.mint(recipient, metadata).send({ from: accounts[0] });
        alert("NFT criado com sucesso!");
    } catch (error) {
        console.error(error);
        alert("Erro ao criar NFT.");
    }
}

// Função para transferir tokens
async function transferTokens() {
    const recipient = document.getElementById("tokenRecipient").value;
    const amount = document.getElementById("tokenAmount").value;

    const accounts = await web3.eth.getAccounts();
    try {
        await tokenContract.methods.transfer(recipient, amount).send({ from: accounts[0] });
        alert("Tokens transferidos com sucesso!");
    } catch (error) {
        console.error(error);
        alert("Erro ao transferir tokens.");
    }
}

